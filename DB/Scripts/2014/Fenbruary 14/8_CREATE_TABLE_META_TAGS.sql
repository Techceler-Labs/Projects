CREATE TABLE `CMS`.`META_TAGS` (
    `META_TAG_ID` INT(11)  NOT NULL AUTO_INCREMENT,
    `ATTRIBUTE` VARCHAR(45) DEFAULT NULL,
   `ATTRIBUTE_VALUE` VARCHAR(500) DEFAULT NULL,
    `ATTRIBUTE_CONTENT_VALUE` VARCHAR(500) DEFAULT NULL,
    `LEVEL`  VARCHAR (45)  DEFAULT NULL,
    `ASSOCIATED_ID` INT(11),
    `SITE_ID` INT(11) NOT NULL,
    PRIMARY KEY (`META_TAG_ID`),
    CONSTRAINT FK_SITE_META_ASSOC
            FOREIGN KEY(`SITE_ID`)  
            REFERENCES SITE(`SITE_ID`)
);