CREATE  TABLE `modules`.`MODULE` (
  `MODULE_ID` INT(11) NOT NULL AUTO_INCREMENT ,
  `NAME` VARCHAR(250) NOT NULL ,
  `DESCRIPTION` VARCHAR(1500) NULL ,
  `UUID` VARCHAR(250) NOT NULL ,
  `IS_ENABLED` BINARY NOT NULL DEFAULT 1 ,
  `MODULE_CATEGORY_ID` INT(11) NOT NULL ,
  `ICON_NAME` VARCHAR(250) NOT NULL ,
  `LISTENER_CLASS` VARCHAR(250) NOT NULL ,
  PRIMARY KEY (`MODULE_ID`) )
COMMENT = 'Table that represents a module in the system';

